class String
  def camelcase
    gsub('/', ' :: ').split(/[ _]/).map(&:capitalize).join
  end

  def underscore
    gsub(/::/, '/').
    gsub(/([A-Z]+)([A-Z][a-z])/,'\1_\2').
    gsub(/([a-z\d])([A-Z])/,'\1_\2').
    tr("-", "_").
    downcase
  end

  def md5
    Digest::MD5.hexdigest(self)
  end

  def set_params(params)
    split('/').map do |slug|
      slug =~ /^:/ && params.delete(slug.delete(':').to_sym) || slug
    end.join('/')
  end

